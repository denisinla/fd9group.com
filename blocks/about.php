        <p>
​By identifying the aesthetic and artistic aspects of fashion and how they can be manipulated to influence consumer response, we can build brands and companies that span fashion tech and which use fresh approaches to the conventional fashion sector.  By bridging the gap between fashion and tech, bringing in relevant experts and resources to collaborate and discover new ways of doing business, we create a platform to foster and create.  In this environment at FD9, art, technology, disruptive goal sets, and maverick ideology all play roles in project developments.  
Data and information are more prevalent than they have ever been in today’s business world, but at FD9 we aim to understand the relevance and insight that they give to the fashion world in each of our projects, making sure to never let fashion insight be dictated or overrun by the data.  
