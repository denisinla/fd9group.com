	<h1>Olivia Crouppen</h1>
	<p>Olivia is responsible for the execution of FD9's photo shoots as well as the sourcing of content for the Company's lookbooks, blog posts and creative content seen online and in print media. Olivia also works with designers during the concept and pre-production processes to finalize designs for production for the besobeso by Luka product line.</p>
	<p>With a background in fine art and fashion design, Olivia’s fascination with the cultural and artistic aspects of fashion has defined her journey. After working as a Los Angeles based fashion stylist and creative consultant Olivia went on to receive her Masters in Fashion Styling from Marangoni London, where she was able to work along-side some of Europe’s most influential creative minds. Upon returning to the U.S. Olivia went on to work in in editorial creative direction where she was able to combine her artistic knowledge and fresh perspective to implement creative solutions which create eye-catching visual concepts. By applying her signature aesthetic to all of her projects ranging from fashion styling and creative direction to graphic design and brand identification, Olivia strives to keep FD9 Group at the forefront of fashion and visual communications.</p>
